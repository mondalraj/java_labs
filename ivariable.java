class ivariable {
    public static void main(String args[]) {
        int iv1 = 10;
        int iv2 = 2;
        System.out.println("!!! Implementing Instance Variables !!!");
        System.out.println("-> Division of two Instance Variables !!!");
        System.out.println("-> A = " + iv1);
        System.out.println("-> B = " + iv2);
        System.out.println("Result -> Division Of A and B is : " + (iv1 / iv2));
    }
}
